function cambiaPiano(piano){
	if(piano == "1"){
		$("#img").attr("src","resources/pontedicoperta.jpg");
		$("#img").attr("alt","ponte di coperta");
	}
	if(piano == "2"){
		$("#img").attr("src","resources/primopontedisovrastruttura.PNG");
		$("#img").attr("alt","primo ponte di sovrastruttura");
	}
	if(piano == "3"){
		$("#img").attr("src","resources/pontedicomando.PNG");
		$("#img").attr("alt","ponte di comando");
	}
	if(piano == "4"){
		$("#img").attr("src","resources/pianof.jpeg");
		$("#img").attr("alt","piano F");
	}
}


